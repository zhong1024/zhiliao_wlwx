public class Shop {

    private String name;
    private String appleName;
    private Integer appleNumber;
    private Double applePay;
    private boolean type;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAppleName() {
        return appleName;
    }

    public void setAppleName(String appleName) {
        this.appleName = appleName;
    }

    public Integer getAppleNumber() {
        return appleNumber;
    }

    public void setAppleNumber(Integer appleNumber) {
        this.appleNumber = appleNumber;
    }

    public Double getApplePay() {
        return applePay;
    }

    public void setApplePay(Double applePay) {
        this.applePay = applePay;
    }

    public boolean isType() {
        return type;
    }

    public void setType(boolean type) {
        this.type = type;
    }
}
